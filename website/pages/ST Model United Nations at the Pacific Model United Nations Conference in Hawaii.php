        And, finally, our entire team won the Best Large Delegation Award, a 1st Place Team Award.
        Please congratulate the entire PacMUN team for their incredible work this week. We are also incredibly proud of our ST graduates Anya Prasad (who was responsible for hosting the entire conference), David Phipps, Paige Watanabe, Jillian Hagman, Pablo Gonzalez, and Kavina Desai who returned to Hawaii to volunteer their time to direct committees.
        As always, thank you for your support of our program.  And thank you, in advance, for your patience as they make up work from their absence.

        Our very best, 
        My Nguyen, Nicohle Boye-Doe, and Bryan Phipps
