<p id="text">Dear Friends and Colleagues, 
        My Nguyen, Nicohle Boye-Doe, and I wanted to share our students' experience at this year's PacMUN conference in Hawaii. 
        
        Despite some COVID jitters from many of our younger delegates, our team of 20 delegates did an incredible job representing Santa Teresa at this year's PacMUN conference.  
        
        Although this was the first time many of our students traveled for school, they behaved marvelously, navigating our activities with grace, intellect, compassion, and a ton of humor.  In committee, our students debated the safe, non-military expansion of the United States into space, human trafficking, gender equality in politics, international sustainable fishing practices, the imperialist expansion of Japan throughout Asia in the 16th century, and the ongoing human rights, health, and military crisis in the Ukraine.  
        
        Outside of committee, our students were hosted at the Paepae o He'eia Fishpond, where they learned about sustainable fish conservation in a traditional Hawaiian preserve, an active site for Hawaiian food for 800 years, enjoyed a private guided evening tour of the Honolulu Zoo, snorkeled in Hanauma Bay, hiked Diamond Head, ate their weight in spam musubi and poke and malasadas and shave ice, and walked dozens of miles around the island, as they learned about the complicated history of colonialism, cultural imperialism, cultural sovereignty, and world war.  These students were an incredible joy to work with.  They returned healthy and COVID free, albeit exhausted!
        
        Please congratulate Santa Teresa's PacMUN team for their incredible success this last week.  Some highlights:
        Verbal Commendations: 
        Adam Gomez (12): Historical Crisis Committee for Japan 
        Zoe Nguyen (12): Historical Crisis Committee for Japan 
