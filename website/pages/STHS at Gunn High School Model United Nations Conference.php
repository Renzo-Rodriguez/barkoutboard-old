        Sophie Chen and Linoy Shalom (Verbal Commendation)--in the US Senate
        Adam Gomez and Emilie Levonius (Verbal Commendation)--UNEA
        Samantha Smith and Nandhini Vijayakumar (Verbal Commendation)--Salem Witch Trial
