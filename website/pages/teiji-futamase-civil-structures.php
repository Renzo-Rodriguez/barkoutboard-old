            <p>
The bridge project was a competition among high school students, run by the MESA program. The objective of the competition was to test the students' abilities to understand civil structural principles by constructing balsa-wood bridges under certain constraints. The students were required to build a bridge entirely out of balsa-wood, weighing no more than 95 grams, with a minimum clearance of 10 cm, a span of 25 cm, and a height of 21 cm. The bridge had to be constructed entirely as a truss-style bridge and secured only by Elmer's glue.

At the MESA Day competition, Santa Teresa High School placed 2nd and 3rd in the 9th/10th division thanks to the efforts of James Vince and Danwoo Yoo, and Sofia Utama and her team. Additionally, the school placed 3rd in the 11th/12th division thanks to the hard work of Koleman Chang and Ashintosh Kumar.</p>
