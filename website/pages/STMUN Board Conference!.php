        Aarushi Arya (Secretary-General), Suyash Jevaria (Director-General), Nandhini Vijayakumar (USG Finances), Zoe Nguyen (USG Delegational Affairs), Kim Le (USG Public Relations), Sophie Chen (USG Records), Sachin Bandhari (Sr. Liaison), Giovanna Lima (Jr. Liaison), Teddy Nguyen (Soph. Liaison), and Jeet Lad (Fresh. Liaison).  
        
